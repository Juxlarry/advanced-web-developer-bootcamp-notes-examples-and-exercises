import React, { Component } from 'react';
import './App.css';


const Box = props => {
  const style = {
    backgroundColor: props.background,
    width: "12.5%",
    height: "100px",
    float: "left"
  }
  return <div style={style} />
}
const boxesNum = 32;
class App extends Component {
  constructor(props){
    super(props);
    const boxes = new Array(boxesNum).fill().map(this.randomColor,this);
    this.state = {boxes};
    
    setInterval(()=>{
      const boxes = [...this.state.boxes];
      const randomIndex = Math.floor(Math.random()*boxes.length);
      boxes[randomIndex] = this.randomColor();
      this.setState({boxes});
    },300)
  }
  randomColor(){
    let colors = this.props.allColors;
    return colors[Math.floor(Math.random()*colors.length)];
  }
  render() {
    const boxes = this.state.boxes.map((box,i)=>(
      <Box key={i} background={box} />
    ))
    return (
      <div className="App">
        {boxes}
      </div>
    );
  }
}

